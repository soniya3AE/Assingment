package ConditionalStatementAssingment;

public class IfConditionAssignment12 {
	//From 1 to 100, print Even and Odd numbers
	public static void main(String[] args) {
		for(int i=0;i<=100;i++) {
			if(i%2==0 ) {
				System.out.println("The given number is even:"+i);
			}
			else
			{
				System.out.println("The given number is Odd:"+i);
			}

		}

}
}
